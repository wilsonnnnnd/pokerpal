type Vars = Record<string, string | number | boolean | null | undefined> | undefined;

function interpolate(template: string, vars?: Vars) {
    if (!vars) return template;
    return template.replace(/\{([a-zA-Z0-9_]+)\}/g, (match, name) => {
        const val = vars[name];
        if (val === null || val === undefined) return '';
        return String(val);
    });
}

// module-level default locale; can be set from SettingsProvider
let defaultLocale: Locale | null = (global as any).__pokerpal_settings?.language ?? 'en';

export function setSimpleTLocale(l: Locale | null) {
    defaultLocale = l;
}

export function simpleT(key: string, locale?: Locale | null, vars?: Vars) {
    const usedLocale = locale ?? defaultLocale ?? (global as any).__pokerpal_settings?.language ?? 'en';
    const bucket = dict[usedLocale] || dict['en'];
    const template = bucket[key] || key;
    return interpolate(template, vars);
}

export default simpleT;
